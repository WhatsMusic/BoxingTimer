class Exercise(var rounds: Int, var duration: Int, var break: Int, var level: String) {

    fun setRounds(rounds: Int) {
        this.rounds = rounds
    }

    fun setDuration(duration: Int) {
        this.duration = duration
    }

    fun setBreak(break: Int) {
        this.break = break
    }

    fun setLevel(level: String) {
        this.level = level
    }
}